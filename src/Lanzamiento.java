
public class Lanzamiento {
	private double distancia;
	private double angulo;
	
	public Lanzamiento(double distancia, double angulo) {
		this.distancia = distancia;
		this.angulo = angulo;
	}

	public double getDistancia() {
		return distancia;
	}

	public double getAngulo() {
		return angulo;
	}
	
	
	
}
